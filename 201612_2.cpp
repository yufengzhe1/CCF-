#include <iostream>

using namespace std;

int main()
{
	int T;
	cin>>T;
	int get;
	
	for(int i=100;i<150000;i=i+100)
	{
		if(i<=3500)
			get=i;
		else
		{
			int a=i-3500;
			if(a<1500)
				get=3500+a*0.97;
			else if(a>=1500&&a<4500)
				get=3500+1500*0.97+(a-1500)*0.9;
			else if(a>=4500&&a<9000)
				get=3500+1500*0.97+3000*0.9+(a-4500)*0.8;
			else if(a>=9000&&a<35000)
				get=3500+1500*0.97+3000*0.9+4500*0.8+(a-9000)*0.75;
			else if(a>=35000&&a<55000)
				get=3500+1500*0.97+3000*0.9+4500*0.8+26000*0.75+(a-35000)*0.7;
			else if(a>=55000&&a<80000)
				get=3500+1500*0.97+3000*0.9+4500*0.8+26000*0.75+20000*0.7+(a-55000)*0.65;
			else if(a>=80000)
				get=3500+1500*0.97+3000*0.9+4500*0.8+26000*0.75+20000*0.7+25000*0.65+(a-80000)*0.55;
		}
		
		if(get==T)
		{
			cout<<i;
			break;
		}
	}
	return 0;
}
